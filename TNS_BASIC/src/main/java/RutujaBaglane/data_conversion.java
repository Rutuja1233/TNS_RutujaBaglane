package RutujaBaglane;

public class data_conversion {
	public static void main (String [] args) {
	System.out.println("hii");
	
	char ch='A';    //convert char into int                  Type casting
	int i =ch;
	System.out.println(i);
	
	double f1=10.65f;    //convert double into long 
	long l1 =(long)f1;
	System.out.println(l1);
	
	
	long l2 = 9876544347642l;    //convert long into int
	int i23 =(int) l2;
	System.out.println(i23);
	
}
}